func TestRenderLog_Idempotent(t *testing.T) {
	d := LogData{Title: "X", Entries: []LogEntry{
		{Date: "2026-06-01", Verb: verbUpdate, FileBase: "a", BundleRelPath: "/x/a.md", Summary: "s", ChangeID: "abcd"},
		{Date: "2026-06-01", Verb: verbUpdate, FileBase: "b", BundleRelPath: "/x/b.md", Summary: "t", ChangeID: "efgh"},
	}}
	if RenderLog(d) != RenderLog(d) {
		t.Error("RenderLog is not byte-stable for identical input")
	}
}

