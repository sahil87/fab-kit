// --- M1 regression: log.md is a generated artifact, never a topic-file row ---

// TestGather_ExcludesGeneratedLogAndIndex pins the M1 fix: a generated log.md
// (and index.md) sitting alongside real topic files must NOT be gathered as a
// topic file — otherwise a second `fab memory-index` run reads the just-written
// log.md back and appends a spurious `[log](log.md)` row to the domain index,
// breaking idempotence/byte-stability (Constitution III). Before the gatherFiles
// fix this asserts FAIL: Files would contain a "log" entry and the rendered
// index would carry a `[log](log.md)` row.
func TestGather_ExcludesGeneratedLogAndIndex(t *testing.T) {
	repo := t.TempDir()
	// Two real topic files plus the two generated, single-writer artifacts.
	writeFile(t, repo, "docs/memory/auth/authentication.md", "# Authentication\n")
	writeFile(t, repo, "docs/memory/auth/sessions.md", "# Sessions\n")
	writeFile(t, repo, "docs/memory/auth/index.md", "# Auth Documentation\n")
	writeFile(t, repo, "docs/memory/auth/log.md",
		"# Log — Auth Documentation\n<!-- Generated by `fab memory-index` ... -->\n")

	_, domains, _, err := Gather(repo)
	if err != nil {
		t.Fatal(err)
	}
	if len(domains) != 1 {
		t.Fatalf("expected one domain, got %d: %+v", len(domains), domains)
	}
	// Only the two real topic files — index.md AND log.md excluded.
	if len(domains[0].Files) != 2 {
		t.Fatalf("expected 2 topic files (index.md + log.md excluded), got %d: %+v",
			len(domains[0].Files), domains[0].Files)
	}
	for _, f := range domains[0].Files {
		if f.Base == "log" || f.Base == "index" {
			t.Errorf("generated artifact %q.md must not be gathered as a topic file", f.Base)
		}
	}
	// And the rendered domain index must carry no `[log]` / `[index]` row.
	out := RenderDomain(domains[0])
	if strings.Contains(out, "[log](log.md)") || strings.Contains(out, "[log](") {
		t.Errorf("domain index must not contain a [log] row, got:\n%s", out)
	}
}

// TestGather_SubDomainExcludesGeneratedLog pins the same exclusion one level
// down — gatherSubDomains delegates to gatherFiles, so it must inherit the fix
// (a sub-domain's log.md must not become a spurious topic row in its index).
func TestGather_SubDomainExcludesGeneratedLog(t *testing.T) {
	repo := t.TempDir()
	writeFile(t, repo, "docs/memory/pipeline/sub/topic.md", "# Topic\n")
	writeFile(t, repo, "docs/memory/pipeline/sub/index.md", "# Sub\n")
	writeFile(t, repo, "docs/memory/pipeline/sub/log.md", "# Log — Sub\n")

	_, domains, _, err := Gather(repo)
	if err != nil {
		t.Fatal(err)
	}
	if len(domains) != 1 || len(domains[0].SubDomains) != 1 {
		t.Fatalf("expected one domain with one sub-domain, got %+v", domains)
	}
	sub := domains[0].SubDomains[0]
	if len(sub.Files) != 1 || sub.Files[0].Base != "topic" {
		t.Fatalf("sub-domain should hold only the real topic file (log.md/index.md excluded), got %+v", sub.Files)
	}
}

// TestMemoryIndex_GenerateThenGenerateIsByteStable is the whole-tree
// idempotence integration test for M1 — the `--check`-equivalent. It builds the
// full target set (root + domain index.md + per-folder log.md) exactly as the
// `fab memory-index` cmd does, writes it to disk (the "first generate"), then
// re-gathers and asserts EVERY target is byte-identical to what is on disk (the
// "second generate" writes nothing → `--check` would exit 0). Before the
// gatherFiles fix the second pass adds a `[log](log.md)` row to auth/index.md,
// so this asserts FAIL. Mirrors the real-git-repo style of
// TestGatherLogs_RealRepo / TestLoadGitDates_BatchEqualsPerFile.
func TestMemoryIndex_GenerateThenGenerateIsByteStable(t *testing.T) {
	repo := t.TempDir()
	gitDateRun(t, repo, "init")
	writeFile(t, repo, "fab/changes/260301-aaaa-first-change/.status.yaml",
		"id: aaaa\nname: 260301-aaaa-first-change\nsummary: \"the curated summary line\"\n")
	writeFile(t, repo, "docs/memory/auth/login.md", "# Login\n")
	writeFile(t, repo, "docs/memory/auth/index.md", "# Auth Documentation\n")
	gitDateRun(t, repo, "add", ".")
	gitDateRun(t, repo, "commit", "-m", "Merge pull request #1 from o/260301-aaaa-first-change",
		"--date", "2026-03-01T12:00:00 +0000")

	fabRoot := filepath.Join(repo, "fab")
	memRoot := filepath.Join(repo, "docs", "memory")

	// buildTargets mirrors the cmd's target assembly: root + domain/sub index.md
	// + per-folder log.md, all from one Gather + GatherLogs pass.
	buildTargets := func() map[string]string {
		root, domains, _, err := Gather(repo)
		if err != nil {
			t.Fatal(err)
		}
		out := map[string]string{
			filepath.Join(memRoot, "index.md"): RenderRoot(root),
		}
		for _, d := range domains {
			out[filepath.Join(memRoot, d.Name, "index.md")] = RenderDomain(d)
			for _, sd := range d.SubDomains {
				out[filepath.Join(memRoot, d.Name, sd.Name, "index.md")] = RenderDomain(sd)
			}
		}
		logTargets, err := GatherLogs(repo, fabRoot)
		if err != nil {
			t.Fatal(err)
		}
		for _, lt := range logTargets {
			out[lt.Path] = lt.Content
		}
		return out
	}

	// First generate: write every target to disk.
	first := buildTargets()
	if _, ok := first[filepath.Join(memRoot, "auth", "log.md")]; !ok {
		t.Fatalf("expected an auth/log.md target on the first generate, got %v", first)
	}
	for path, content := range first {
		if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
			t.Fatal(err)
		}
	}

	// Second generate: re-gather against the now-fully-generated tree and assert
	// every target is byte-identical to what is on disk (nothing to write).
	second := buildTargets()
	for path, content := range second {
		existing, err := os.ReadFile(path)
		if err != nil {
			t.Fatalf("second-pass target %s missing from disk: %v", path, err)
		}
		if string(existing) != content {
			relPath, _ := filepath.Rel(repo, path)
			t.Errorf("NOT idempotent: second generate of %s differs from the first.\n--- on disk ---\n%s\n--- re-rendered ---\n%s",
				filepath.ToSlash(relPath), existing, content)
		}
	}
	// Belt-and-suspenders: the auth domain index must never gain a [log] row on
	// the second pass (the exact M1 symptom).
	if got := second[filepath.Join(memRoot, "auth", "index.md")]; strings.Contains(got, "[log](log.md)") {
		t.Errorf("second generate added a spurious [log] row to auth/index.md:\n%s", got)
	}
}
