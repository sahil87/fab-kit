// --- RenderLog pure renderer (byte-for-byte, FKF §6.2) ---------------------

func TestGolden_RenderLog_FullDocument(t *testing.T) {
	got := RenderLog(LogData{
		Title: "Distribution Documentation",
		Entries: []LogEntry{
			{Date: "2026-06-13", Verb: verbUpdate, FileBase: "migrations", BundleRelPath: "/distribution/migrations.md",
				Summary: "surfaces the optional agent.tiers override as a commented config block", ChangeID: "l3ja"},
			{Date: "2026-06-12", Verb: verbUpdate, FileBase: "migrations", BundleRelPath: "/distribution/migrations.md",
				Summary: "drops the dead stage_directives block", ChangeID: "c5tr"},
			{Date: "2026-06-12", Verb: verbCreation, FileBase: "setup", BundleRelPath: "/distribution/setup.md",
				Summary: "adds the setup doc", ChangeID: "tb6f"},
		},
	})
	want := "# Log — Distribution Documentation\n" +
		"<!-- Generated by `fab memory-index` from git history + per-change summaries. Do not hand-edit. -->\n" +
		"\n" +
		"## 2026-06-13\n" +
		"- **Update** [migrations](/distribution/migrations.md) — surfaces the optional agent.tiers override as a commented config block (l3ja)\n" +
		"\n" +
		"## 2026-06-12\n" +
		"- **Update** [migrations](/distribution/migrations.md) — drops the dead stage_directives block (c5tr)\n" +
		"- **Creation** [setup](/distribution/setup.md) — adds the setup doc (tb6f)\n"
	if got != want {
		t.Errorf("RenderLog full-document golden mismatch.\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

func TestRenderLog_DateGroupingNewestFirst(t *testing.T) {
	// Out-of-order input must render newest-date-first regardless of input order.
	got := RenderLog(LogData{Title: "X", Entries: []LogEntry{
		{Date: "2026-01-01", FileBase: "a", BundleRelPath: "/x/a.md", Summary: "old", ChangeID: "aaaa"},
		{Date: "2026-12-31", FileBase: "b", BundleRelPath: "/x/b.md", Summary: "new", ChangeID: "bbbb"},
	}})
	iNew := strings.Index(got, "## 2026-12-31")
	iOld := strings.Index(got, "## 2026-01-01")
	if iNew < 0 || iOld < 0 || iNew > iOld {
		t.Errorf("newest date must come first:\n%s", got)
	}
}

