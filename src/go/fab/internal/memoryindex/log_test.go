// --- GatherLogs integration on a real git repo -----------------------------

// TestGatherLogs_RealRepo exercises the full C-lite join end-to-end: a merge
// commit whose subject names a registered change → attributed (id + slug),
// versus a squash-style commit with no recoverable token → graceful degradation
// (no id, empty/missing-cell line). Mirrors TestLoadGitDates_BatchEqualsPerFile.
func TestGatherLogs_RealRepo(t *testing.T) {
	repo := t.TempDir()
	gitDateRun(t, repo, "init")

	// Registry: one registered change with a summary.
	writeFile(t, repo, "fab/changes/260301-aaaa-first-change/.status.yaml",
		"id: aaaa\nname: 260301-aaaa-first-change\nsummary: \"the curated summary line\"\n")

	// Commit 1: attributable via a merge-style subject naming the folder.
	writeFile(t, repo, "docs/memory/auth/login.md", "# Login\n")
	gitDateRun(t, repo, "add", ".")
	gitDateRun(t, repo, "commit", "-m", "Merge pull request #1 from o/260301-aaaa-first-change",
		"--date", "2026-03-01T12:00:00 +0000")

	// Commit 2: a later squash-style edit with no recoverable token → degrades.
	writeFile(t, repo, "docs/memory/auth/login.md", "# Login updated\n")
	gitDateRun(t, repo, "add", ".")
	gitDateRun(t, repo, "commit", "-m", "fix: tweak login (#42)", "--date", "2026-04-01T12:00:00 +0000")

	targets, err := GatherLogs(repo, filepath.Join(repo, "fab"))
	if err != nil {
		t.Fatal(err)
	}
	var authLog *LogTarget
	for i := range targets {
		if strings.HasSuffix(filepath.ToSlash(targets[i].Path), "docs/memory/auth/log.md") {
			authLog = &targets[i]
		}
	}
	if authLog == nil {
		t.Fatalf("expected an auth/log.md target, got %d targets: %+v", len(targets), targets)
	}
	content := authLog.Content
	// Newest (squash, unattributable) entry: no (id); the commit subject is the
	// graceful-degradation descriptive line (R7); Update verb.
	if !strings.Contains(content, "## 2026-04-01") {
		t.Errorf("expected the 2026-04-01 squash commit date heading, got:\n%s", content)
	}
	if !strings.Contains(content, "- **Update** [login](/auth/login.md) — fix: tweak login (#42)\n") {
		t.Errorf("unattributable commit should degrade to the commit subject (no id), got:\n%s", content)
	}
	if strings.Contains(content, "(#42))") {
		t.Errorf("unattributable commit must not synthesize a (change-id) token, got:\n%s", content)
	}
	// Older (attributed) entry: (aaaa) + the curated summary, Creation verb (added).
	if !strings.Contains(content, "## 2026-03-01") {
		t.Errorf("expected the 2026-03-01 attributed commit date heading, got:\n%s", content)
	}
	if !strings.Contains(content, "- **Creation** [login](/auth/login.md) — the curated summary line (aaaa)\n") {
		t.Errorf("attributed commit should carry summary + (id), got:\n%s", content)
	}
}

