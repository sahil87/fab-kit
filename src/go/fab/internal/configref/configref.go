// agentTiersSegment renders the agent.tiers block. The FIXED stage→tier mapping
// comment and the built-in default profiles are both emitted from the single
// tierRows() walk — the same rows the agent.tiers Default is built from — so no
// tier value is a literal copy and the loop is not duplicated.
func agentTiersSegment(tiers []tierRow) string {
	var b strings.Builder
	b.WriteString("# agent.tiers — per-stage model override. A \"tier\" is a named\n")
	b.WriteString("# {provider, model, effort} profile (the invocation command lives on the provider,\n")
	b.WriteString("# above — NOT on the tier). fab-kit owns the FIXED, non-overridable stage→tier\n")
	b.WriteString("# mapping below; you override only WHAT EACH TIER MEANS. Omit any tier (or the\n")
	b.WriteString("# whole tiers: block) to use fab-kit's built-in default. An omitted field within a\n")
	b.WriteString("# tier inherits the project's `default` tier, then fab-kit's built-in; an empty\n")
	b.WriteString("# model means \"inherit the session model\". Resolved per stage by\n")
	b.WriteString("# `fab resolve-agent <stage>` at sub-agent dispatch time. Values pass through\n")
	b.WriteString("# verbatim — fab does no provider validation. See docs/specs/stage-models.md.\n")
	b.WriteString("#\n")
	b.WriteString("# FIXED stage→tier mapping (fab-owned, NOT overridable — shown for reference):\n")
	for _, t := range tiers {
		fmt.Fprintf(&b, "#   %-9s %s\n", t.Name, t.Stages)
	}
	b.WriteString("#\n")
	b.WriteString("# fab-kit's built-in default profiles (today):\n")
	b.WriteString("agent:\n")
	b.WriteString("  tiers:\n")
	for _, t := range tiers {
		fmt.Fprintf(&b, "    %-9s { provider: %s, model: %s, effort: %s }\n",
			t.Name+":", t.Profile.Provider, t.Profile.Model, t.Profile.Effort)
	}
	// Trim the trailing newline: Render() re-inserts the inter-field separator.
	return strings.TrimRight(b.String(), "\n")
}

// stageHooksSegment renders the stage_hooks block. The valid stage-keys list is
// interpolated from agent.StageNames() (sorted, deterministic) — no literal copy
// of the stage set.
func stageHooksSegment() string {
	return "# stage_hooks — optional per-stage pre/post shell commands honored by\n" +
		"# `fab status start` / `fab status finish`. Each command runs as `sh -c` from\n" +
		"# the repo root. A failing `pre` hook blocks `fab status start`; a `post` hook\n" +
		"# runs after `finish` saves. Not seeded by the scaffold — add by hand.\n" +
		"# Valid stage keys: " + strings.Join(agent.StageNames(), ", ") + ".\n" +
		"# stage_hooks:\n" +
		"#   apply:\n" +
		"#     pre: ./scripts/check-clean-tree.sh\n" +
		"#     post: make test"
}

// lintFields is the fail-loud registry validation: every row must have a
// non-empty Description and a valid Scope. A violation is a construction bug
// (a new row added without metadata), caught here rather than shipped as a
// degraded reference — the same discipline as the tier-profile invariant above.
func lintFields(fields []Field) error {
	for _, f := range fields {
		if f.Description == "" {
			return fmt.Errorf("configref: field %q has an empty description (every registry row must document its field)", f.Key)
		}
		if !validScope(f.Scope) {
			return fmt.Errorf("configref: field %q has invalid scope %q (want one of project/system/both)", f.Key, f.Scope)
		}
	}
	return nil
}

// FieldKeys returns the dotted key paths of every registry row, in table order.
// Used by the JSON/YAML key-parity guard and any external consumer needing the
// documented key set.
func FieldKeys() ([]string, error) {
	fields, err := Fields()
	if err != nil {
		return nil, err
	}
	keys := make([]string, len(fields))
	for i, f := range fields {
		keys[i] = f.Key
	}
	return keys, nil
}

// Render returns the fully-commented reference config.yaml, generated by walking
// the field registry: the fixed header followed by each row's rendered Segment in
// table order, one blank line between blocks. The output is byte-stable for a
// given binary version and is what `fab config reference` prints to stdout. It
// returns an error rather than emitting partial/degraded output if an invariant
// breaks (Fields surfaces the tier-map drift and the registry lint), so a future
// edit that breaks an invariant fails loudly instead of silently shipping a
// malformed reference.
//
// The emitted text is contract-equivalent to the pre-metadata-table renderer
// (same key coverage, live/commented split, and every documented-semantics prose
// block the tests assert verbatim) — the restructure changed the SOURCE of the
// reference (prose template → metadata table walked here), not its output
// contract.
func Render() (string, error) {
	fields, err := Fields()
	if err != nil {
		return "", err
	}
	var b strings.Builder
	b.WriteString(referenceHeader)
	for _, f := range fields {
		if f.Segment == "" {
			// A field rendered inside another field's block (e.g. project.description
			// / project.linear_workspace live in the project.name segment) carries no
			// segment of its own — skip it in the walk.
			continue
		}
		b.WriteString("\n\n")
		b.WriteString(f.Segment)
	}
	b.WriteString("\n")
	return b.String(), nil
}

// RenderJSON returns the field registry as a machine-readable JSON array — the
// tooling surface Changes 2-3 and external tools consume. It is a flat array in
// table (rendering) order, each element a per-field object
// {key, default, description, scope, advertise, renamed_from(omitempty)}.
// Deterministic and byte-stable like Render(): the table is ordered and the
// marshalling is stable. Two-space indented with a trailing newline. Segment is
// excluded — it is the YAML rendering, not part of the machine-readable schema.
func RenderJSON() (string, error) {
	fields, err := Fields()
	if err != nil {
		return "", err
	}
	view := make([]jsonField, len(fields))
	for i, f := range fields {
		view[i] = jsonField{
			Key:         f.Key,
			Default:     f.Default,
			Description: f.Description,
			Scope:       f.Scope,
			Advertise:   f.Advertise,
			RenamedFrom: f.RenamedFrom,
		}
	}
	var buf bytes.Buffer
	enc := json.NewEncoder(&buf)
	enc.SetIndent("", "  ")
	enc.SetEscapeHTML(false)
	if err := enc.Encode(view); err != nil {
		return "", fmt.Errorf("configref: marshalling reference JSON: %w", err)
	}
	return buf.String(), nil
}
