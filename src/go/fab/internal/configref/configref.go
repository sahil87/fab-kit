// Package configref generates the fully-commented reference config.yaml that
// `fab config reference` prints to stdout, and exposes the same schema as a
// machine-readable field table (`--json`). The reference is the single
// discoverability surface for the config.yaml schema — it documents BOTH the
// binary-consumed keys (modeled on internal/config.Config) and the
// skill-consumed keys (read by markdown skills, invisible to Go reflection).
//
// METADATA TABLE, NOT A PROSE TEMPLATE. The schema is modeled as an ordered
// per-field metadata table (the Fields registry): each entry carries the field's
// canonical default, one-line description, scope (project/system/both), advertise
// flag, renamed_from carry-forward, AND the rendered YAML segment (the commented
// block that documents the field in the reference). Both renderings — the
// commented YAML and the JSON dump — are generated by walking that one table:
// Render() concatenates each row's Segment in table order, RenderJSON() marshals
// each row's metadata. There is no second copy of the schema and no monolithic
// template to drift against the table. This is Change 1 of the config-upgrade
// effort; the scope/advertise/renamed_from metadata is consumed by the downstream
// cascade resolver, `fab config init --system`, and `fab config upgrade`
// (Changes 2-3). See docs/specs/config.md.
//
// GENERATED, NOT HAND-WRITTEN. Every default value that has a canonical Go
// constant is sourced from that constant, never copied — the default provider
// session command (agent.DefaultSessionCommand), the per-tier default profiles
// via agent.DefaultTier over agent.TierNames, and the pipeline stage names via
// agent.StageNames. The dynamic segments (providers, agent.tiers, stage_hooks)
// interpolate those same constants when the row is built, so the reference text
// carries no literal copy of any value.
//
// Canonical default vs. rendering example: a field's Default holds the CANONICAL
// built-in default (what the downstream cascade falls back to), not the value the
// reference happens to show as an example. source_paths/test_paths render an
// example (`- src/`, `- "**/*_test.go"`) but their binary default is empty, so
// their Default is nil — the example lives in the rendered Segment, not in the
// metadata.
//
// Empty-default convention: a field with no meaningful built-in default carries a
// nil Default (JSON `null`), NOT a typed empty (`[]`, `{}`, `""`). The nil is the
// uniform "no built-in default — the cascade falls back to absent" signal the
// downstream resolver (Change 2) consumes; distinguishing []string{} from nil
// would leak an implementation detail with no cascade meaning. A non-nil Default
// therefore always denotes a real built-in value (the claude provider, the five
// tier profiles). See docs/specs/config.md § Default semantics.
//
// Render() output is BYTE-STABLE for a given binary version: the field table is
// fixed and ordered, the interpolated tier/stage lists come from the
// already-sorted agent.TierNames/agent.StageNames accessors, and no map is
// range-iterated during rendering. RenderJSON() is byte-stable for the same
// reasons (ordered table, deterministic marshalling).
package configref

import (
	"bytes"
	"encoding/json"
	"fmt"
	"strings"

	"github.com/sahil87/fab-kit/src/go/fab/internal/agent"
)

// Scope is a field's override visibility across the config cascade layers. It is
// data-only in this change (no behavioral consumer): the cascade resolver and
// `fab config init --system` that key on it land in Change 2. See
// docs/specs/config.md § Scope taxonomy.
type Scope string

const (
	// ScopeProject: overridable only in the project file (fab/project/config.yaml).
	// Semantics-class fields stay repo-reproducible for teammates/CI.
	ScopeProject Scope = "project"
	// ScopeSystem: overridable only in the system file (~/.fab-kit/config.yaml).
	// (No field is system-only today; the value exists for completeness/Change 2.)
	ScopeSystem Scope = "system"
	// ScopeBoth: overridable in either layer (preference-class fields).
	ScopeBoth Scope = "both"
)

// validScope reports whether s is one of the three known scope values. Used by
// the registry lint (a scope outside this set is a fail-loud construction error).
func validScope(s Scope) bool {
	switch s {
	case ScopeProject, ScopeSystem, ScopeBoth:
		return true
	default:
		return false
	}
}

// Field is one row of the per-field metadata table — the single source the two
// renderings (commented YAML, JSON) are both generated from. It models the
// override UNIT (a meaningful override surface), which is coarser than a leaf
// key: map-valued fields (providers, agent.tiers, stage_hooks) are single rows
// with structured defaults, matching the downstream per-field deep-merge (maps
// merge per-key, lists replace, scalars replace).
type Field struct {
	// Key is the dotted path of the override surface (e.g. "agent.tiers",
	// "project.name", "true_impact_exclude"). It is the identity used by the
	// JSON dump and the JSON/YAML key-parity check.
	Key string
	// Default is the CANONICAL built-in default (typed), NOT a rendering example.
	// nil means "no built-in default" (the cascade falls back to empty/absent) —
	// e.g. source_paths/test_paths, whose shown values are examples only. A typed
	// empty is never used for "no default"; see the package-doc empty-default
	// convention.
	Default any
	// Description is the one-line summary of the field. Required (non-empty) —
	// the registry lint rejects an empty description. It feeds the JSON dump and
	// the future generated comment scaffold (Change 3). This is the short summary;
	// the long-form documentation prose lives in Segment (the rendered YAML block).
	Description string
	// Scope is the field's override visibility (project/system/both). Data-only
	// here; enforced in Change 2.
	Scope Scope
	// Advertise is the "C flag": whether Change 3's managed fence scaffolds this
	// field as a commented reference when it is not overridden. NO behavioral
	// consumer in this change — data + JSON exposure only.
	Advertise bool
	// RenamedFrom is the previous key path for mechanical rename carry-forward.
	// "" for every row today; the field serves FUTURE renames so they stop needing
	// hand-written migrations. Omitted from the JSON dump when empty.
	RenamedFrom string
	// Segment is the rendered commented-YAML block that documents this field in
	// the reference — the field's own lines (leading comment prose + the live or
	// commented key), WITHOUT a trailing newline or the inter-field blank line.
	// Render() concatenates the segments in table order (blank line between). For
	// the constant-backed fields (providers, agent.tiers, stage_hooks) the segment
	// is built by interpolating the same Go constants Default reads, so the
	// rendered text carries no literal copy of any value. Not exposed in the JSON
	// dump (it is the human-readable rendering of the same metadata).
	Segment string
}

// jsonField is the JSON projection of a Field: stable snake_case keys and
// renamed_from,omitempty (empty on every row today, so it is absent from the
// current dump). Segment is intentionally excluded — it is the YAML rendering of
// the same metadata, not part of the machine-readable schema. Kept separate from
// Field so the Go struct-field names stay idiomatic while the wire shape stays
// snake_case and stable for Changes 2-3.
type jsonField struct {
	Key         string `json:"key"`
	Default     any    `json:"default"`
	Description string `json:"description"`
	Scope       Scope  `json:"scope"`
	Advertise   bool   `json:"advertise"`
	RenamedFrom string `json:"renamed_from,omitempty"`
}

// tierStages is the human-readable stage grouping shown next to each tier in the
// reference comment. It restates the FIXED stage→tier mapping owned by
// internal/agent (default={intake advisory}, operator={fab operator},
// doing={apply,review-pr,hydrate}, review={review}, fast={ship}) — reference prose
// only, not a behavioral second source.
var tierStages = map[string]string{
	agent.TierDefault:  "intake (advisory), fab batch, fab agent",
	agent.TierOperator: "fab operator (coordinator session)",
	agent.TierDoing:    "apply, review-pr, hydrate",
	agent.TierReview:   "review",
	agent.TierFast:     "ship",
}

// tierProfileDefault is the structured canonical default for one agent tier —
// what lands in the agent.tiers row's Default (and the JSON dump). Sourced from
// agent.DefaultTier; never a literal copy.
type tierProfileDefault struct {
	Provider string `json:"provider"`
	Model    string `json:"model"`
	Effort   string `json:"effort"`
}

// providerDefault is the structured canonical default for the providers row: the
// built-in claude provider with its session command (dispatch_command absent =
// native Agent-tool dispatch). Sourced from agent.DefaultSessionCommand.
type providerDefault struct {
	SessionCommand string `json:"session_command"`
}

// tierRow is one resolved tier: its name, its fixed stage grouping (reference
// prose from tierStages), and its default {provider, model, effort} profile
// (from agent.DefaultTier). It is the single per-tier view both the agent.tiers
// Default (as tierProfileDefault) and the agent.tiers Segment are built from —
// one walk of agent.TierNames(), no duplicated loop.
type tierRow struct {
	Name    string
	Stages  string
	Profile tierProfileDefault
}

// tierRows returns the ordered per-tier view, one row per agent.TierNames()
// entry (sorted, so deterministic). It is the SINGLE source that both the
// agent.tiers Default and the agent.tiers rendered Segment consume — there is no
// second TierNames/DefaultTier loop. It fails loud on either tier-map drift: a
// tier agent.TierNames reports must have an agent.DefaultTier profile AND a
// tierStages grouping (the reference comment relies on both).
func tierRows() ([]tierRow, error) {
	names := agent.TierNames()
	rows := make([]tierRow, 0, len(names))
	for _, name := range names {
		p, ok := agent.DefaultTier(name)
		if !ok {
			return nil, fmt.Errorf("configref: tier %q from agent.TierNames has no agent.DefaultTier profile", name)
		}
		stages, ok := tierStages[name]
		if !ok {
			return nil, fmt.Errorf("configref: tier %q has no tierStages grouping (add one when adding a tier)", name)
		}
		rows = append(rows, tierRow{
			Name:    name,
			Stages:  stages,
			Profile: tierProfileDefault{Provider: p.Provider, Model: p.Model, Effort: p.Effort},
		})
	}
	return rows, nil
}

// tierProfileDefaults projects tierRows into the map keyed by tier name that the
// agent.tiers Field.Default carries (and the JSON dump emits).
func tierProfileDefaults(rows []tierRow) map[string]tierProfileDefault {
	out := make(map[string]tierProfileDefault, len(rows))
	for _, r := range rows {
		out[r.Name] = r.Profile
	}
	return out
}

